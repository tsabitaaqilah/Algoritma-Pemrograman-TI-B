#memasukkan nilai dari user ke dalam variabel
nilai = int(input('Masukkan nilai ujian (0-100):'))

#mengecek apakah lulus atau tidak lulus
if nilai >= 60:
    print('Lulus') #ini akan di tampilkan jika nilai lebih dari sama dengan 60
else:
    print('Tidak Lulus') #ini akan di tampilkan jika nilai tidak lebih dari sama dengan 60